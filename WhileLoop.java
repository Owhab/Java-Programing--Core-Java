class WhileLoop{
    public static void main(String []Args)
    {
        
        System.out.println("Now, We are going to learn about While Loop in Java");
        
        int i = 1;
        
        while(i <= 5000)
        {
            System.out.println(i+ ". Welcome to Java Programing");
            
            i++;
            
        }
        
        
    }
}