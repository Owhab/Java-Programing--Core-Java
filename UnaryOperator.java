class UnaryOperator{
    public static void main(String []Args )
    {
        
        System.out.println("This Lesson is about Unary Operator");
        
        
        int a, b, c, result1, result2, result3; 
        
        
        a = 5;
        b = 10;
        c = 15;
        
        result1 = +a;
        System.out.println("The Value of Unary Plus Operand a is: "+result1);
        result1 = -a;
        System.out.println("The value of Unary Minus Operand a is: "+result1);
        result2 = +b;
        System.out.println("The Value of Unary Plus Operand b is: "+result2);
        result2 = -b;
        System.out.println("The value of Unary Minus Operand b is: "+result2);
        result3 = +c;
        System.out.println("The value of Unary Plus Operand c is: "+result3);
        result3 = -c;
        System.out.println("The value of Unary Minus Operand c is: "+result3);
        
        
        
        
        
    }
    
    
    
    
}